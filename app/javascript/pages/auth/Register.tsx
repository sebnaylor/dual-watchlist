import React from "react";
import { useForm } from "@inertiajs/react";

interface RegisterProps {
  errors?: {
    email?: string[];
    password?: string[];
    password_confirmation?: string[];
    first_name?: string[];
    last_name?: string[];
  };
  csrfToken: string;
}

export default function Register({ errors, csrfToken }: RegisterProps) {
  const { data, setData, post, processing } = useForm({
    email: "",
    first_name: "",
    last_name: "",
    password: "",
    password_confirmation: "",
    image: null as File | null,
  });

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    post("/users", {
      forceFormData: true,
    });
  };

  return (
    <div className="min-h-screen flex">
      <div className="hidden lg:flex lg:w-1/2 relative overflow-hidden">
        <div
          className="absolute inset-0 bg-cover bg-center"
          style={{
            backgroundImage:
              "url('https://images.unsplash.com/photo-1536440136628-849c177e76a1?auto=format&fit=crop&w=1920&q=80')",
          }}
        />
        <div className="absolute inset-0 bg-gradient-to-br from-brand-accent/90 to-brand-primary/80" />
        <div className="relative z-10 flex flex-col justify-center p-12 xl:p-16">
          <div className="flex items-center gap-4 mb-8">
            <img
              className="w-14 h-14"
              src="https://dual-watchlist.s3.eu-north-1.amazonaws.com/logo.png"
              alt="logo"
            />
            <h1 className="text-3xl font-titleFont text-white">What To Watch</h1>
          </div>
          <h2 className="text-4xl xl:text-5xl font-bold text-white mb-6 leading-tight">
            Start tracking
            <br />
            what you watch
          </h2>
          <p className="text-white/80 text-lg max-w-md">
            Join thousands of couples who use What To Watch to manage their movie nights together.
          </p>
        </div>
      </div>

      <div className="w-full lg:w-1/2 flex items-center justify-center p-6 md:p-12 bg-theme-primary overflow-y-auto">
        <div className="w-full max-w-md">
          <div className="lg:hidden flex items-center gap-3 mb-8">
            <img
              className="w-10 h-10"
              src="https://dual-watchlist.s3.eu-north-1.amazonaws.com/logo.png"
              alt="logo"
            />
            <h1 className="text-xl font-titleFont text-theme-primary">What To Watch</h1>
          </div>

          <h2 className="text-2xl md:text-3xl font-bold text-theme-primary mb-2">
            Create your account
          </h2>
          <p className="text-theme-secondary mb-8">
            Start tracking movies with your partner today
          </p>

          <form onSubmit={handleSubmit} className="space-y-4">
            <div>
              <label className="block text-sm font-medium text-theme-primary mb-2">
                Email address
              </label>
              <input
                type="email"
                value={data.email}
                onChange={(e) => setData("email", e.target.value)}
                autoFocus
                autoComplete="email"
                placeholder="you@example.com"
                className="w-full px-4 py-3 rounded-lg input-theme border border-theme focus:border-brand-primary focus:ring-2 focus:ring-brand-primary/20 transition-all outline-none"
              />
              {errors?.email && (
                <p className="text-red-500 text-sm mt-2">{errors.email[0]}</p>
              )}
            </div>

            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-theme-primary mb-2">
                  First name
                </label>
                <input
                  type="text"
                  value={data.first_name}
                  onChange={(e) => setData("first_name", e.target.value)}
                  autoComplete="given-name"
                  placeholder="John"
                  className="w-full px-4 py-3 rounded-lg input-theme border border-theme focus:border-brand-primary focus:ring-2 focus:ring-brand-primary/20 transition-all outline-none"
                />
                {errors?.first_name && (
                  <p className="text-red-500 text-sm mt-2">{errors.first_name[0]}</p>
                )}
              </div>

              <div>
                <label className="block text-sm font-medium text-theme-primary mb-2">
                  Last name
                </label>
                <input
                  type="text"
                  value={data.last_name}
                  onChange={(e) => setData("last_name", e.target.value)}
                  autoComplete="family-name"
                  placeholder="Doe"
                  className="w-full px-4 py-3 rounded-lg input-theme border border-theme focus:border-brand-primary focus:ring-2 focus:ring-brand-primary/20 transition-all outline-none"
                />
                {errors?.last_name && (
                  <p className="text-red-500 text-sm mt-2">{errors.last_name[0]}</p>
                )}
              </div>
            </div>

            <div>
              <label className="block text-sm font-medium text-theme-primary mb-2">
                Profile photo (optional)
              </label>
              <input
                type="file"
                accept="image/*"
                onChange={(e) => setData("image", e.target.files?.[0] || null)}
                className="w-full px-4 py-3 rounded-lg input-theme border border-theme text-theme-secondary file:mr-4 file:py-2 file:px-4 file:rounded-lg file:border-0 file:text-sm file:font-medium file:bg-brand-primary file:text-white hover:file:bg-brand-primary/90"
              />
            </div>

            <div>
              <label className="block text-sm font-medium text-theme-primary mb-2">
                Password
              </label>
              <input
                type="password"
                value={data.password}
                onChange={(e) => setData("password", e.target.value)}
                autoComplete="new-password"
                placeholder="Create a strong password"
                className="w-full px-4 py-3 rounded-lg input-theme border border-theme focus:border-brand-primary focus:ring-2 focus:ring-brand-primary/20 transition-all outline-none"
              />
              {errors?.password && (
                <p className="text-red-500 text-sm mt-2">{errors.password[0]}</p>
              )}
            </div>

            <div>
              <label className="block text-sm font-medium text-theme-primary mb-2">
                Confirm password
              </label>
              <input
                type="password"
                value={data.password_confirmation}
                onChange={(e) => setData("password_confirmation", e.target.value)}
                autoComplete="new-password"
                placeholder="Confirm your password"
                className="w-full px-4 py-3 rounded-lg input-theme border border-theme focus:border-brand-primary focus:ring-2 focus:ring-brand-primary/20 transition-all outline-none"
              />
              {errors?.password_confirmation && (
                <p className="text-red-500 text-sm mt-2">
                  {errors.password_confirmation[0]}
                </p>
              )}
            </div>

            <button
              type="submit"
              disabled={processing}
              className="w-full py-3 px-4 bg-brand-primary hover:bg-brand-primary/90 text-white font-medium rounded-lg transition-all disabled:opacity-50 disabled:cursor-not-allowed mt-2"
            >
              {processing ? "Creating account..." : "Create account"}
            </button>
          </form>

          <div className="relative my-6">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-theme" />
            </div>
            <div className="relative flex justify-center text-sm">
              <span className="px-4 bg-theme-primary text-theme-muted">Or continue with</span>
            </div>
          </div>

          <form action="/users/auth/google_oauth2" method="post">
            <input type="hidden" name="authenticity_token" value={csrfToken} />
            <button
              type="submit"
              className="w-full flex items-center justify-center gap-3 py-3 px-4 bg-theme-secondary border border-theme rounded-lg font-medium text-theme-primary hover:bg-theme-tertiary transition-all"
            >
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="#34A853"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="#FBBC05"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="#EA4335"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
              Sign up with Google
            </button>
          </form>

          <p className="text-center text-theme-secondary mt-6">
            Already have an account?{" "}
            <a
              href="/users/sign_in"
              className="text-brand-primary hover:text-brand-accent font-medium transition-colors"
            >
              Sign in
            </a>
          </p>
        </div>
      </div>
    </div>
  );
}

Register.layout = (page: React.ReactNode) => page;
